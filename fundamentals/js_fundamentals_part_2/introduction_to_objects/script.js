// Object is data structure in JS

// Object literal syntax
const jonas = {
    firstName: 'Jonas',
    lastName: 'Peter',
    age: 2021 - 1991,
    job: 'teacher'
};

// Each key inside object is called as property
// jonas object has 4 properties

console.log(jonas); // {firstName: "Jonas", lastName: "Peter", age: 30, job: "teacher"}
console.log(jonas.firstName); // Jonas

// order of properties does not matter in objects